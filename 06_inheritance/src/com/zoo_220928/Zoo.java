package com.zoo_220928;

public class Zoo {
	public void tiger() { 
		System.out.println("무서운 호랑이");
	}
	protected void giraffe() {
		System.out.println("목이 긴 기린");
	}
	void elephant() {
		System.out.println("뚱뚱한 코끼리");
	}
	private void lion() {
		System.out.println("멋진 사자");
	}
	

}
/*
접근제어자
public 문을 열어놨다
private 는 같은 class 내에서만
protected 나와 자식 클래스만 허용
void만 있으면 dafault 아무것도 안됨

 
*/